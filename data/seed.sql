-- ============================================================
-- SaaS Metrics Sample Dataset
-- ============================================================

CREATE TABLE IF NOT EXISTS companies (
    company_id    SERIAL PRIMARY KEY,
    name          VARCHAR(100) NOT NULL,
    industry      VARCHAR(50),
    plan          VARCHAR(20) CHECK (plan IN ('starter', 'growth', 'enterprise')),
    mrr           NUMERIC(10,2),
    signup_date   DATE,
    churned_date  DATE
);

CREATE TABLE IF NOT EXISTS subscriptions (
    subscription_id SERIAL PRIMARY KEY,
    company_id      INT REFERENCES companies(company_id),
    plan            VARCHAR(20) CHECK (plan IN ('starter', 'growth', 'enterprise')),
    mrr             NUMERIC(10,2),
    start_date      DATE,
    end_date        DATE,
    status          VARCHAR(20) CHECK (status IN ('active', 'cancelled', 'upgraded', 'downgraded'))
);

CREATE TABLE IF NOT EXISTS events (
    event_id    SERIAL PRIMARY KEY,
    company_id  INT REFERENCES companies(company_id),
    event_type  VARCHAR(30) CHECK (event_type IN ('signup', 'upgrade', 'downgrade', 'churn', 'reactivation')),
    event_date  DATE,
    from_plan   VARCHAR(20),
    to_plan     VARCHAR(20)
);

CREATE TABLE IF NOT EXISTS invoices (
    invoice_id  SERIAL PRIMARY KEY,
    company_id  INT REFERENCES companies(company_id),
    amount      NUMERIC(10,2),
    invoice_date DATE,
    due_date    DATE,
    status      VARCHAR(20) CHECK (status IN ('paid', 'pending', 'overdue', 'failed'))
);

